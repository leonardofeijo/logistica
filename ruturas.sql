SELECT 
  DE100M.USRLD3, 
  count(DE100M.USRLD3), 
  DE100M.USRSD3, 
  TSA015.SA1506, 
  TSA015.SA1511, 
  TSA015.SA1512, 
  MIN(FET001K.ETK192), 
  (SELECT (IC130M_0.LLBLT1+IC130M_0.LLBLT2+IC130M_0.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_0 WHERE (IC130M_0.LLPPN=DE100M.USRLD3) AND (IC130M_0.LLSTLC='QUALID')), 
  (IC130M.LLBLT1+IC130M.LLBLT2+IC130M.LLBLT4), 
  (IC140M.WBBLT4-(IC130M.LLBLT1+IC130M.LLBLT2+IC130M.LLBLT4)),
  (SELECT (IC130M_1.LLBLT1+IC130M_1.LLBLT2+IC130M_1.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_1 WHERE (IC130M_1.LLPPN=DE100M.USRLD3) AND (IC130M_1.LLSTLC='MPPORTO')), 
  (SELECT (IC130M_2.LLBLT1+IC130M_2.LLBLT2+IC130M_2.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_2 WHERE (IC130M_2.LLPPN=DE100M.USRLD3) AND (IC130M_2.LLSTLC='MP-KANB')), 
  (SELECT (IC130M_3.LLBLT1+IC130M_3.LLBLT2+IC130M_3.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_3 WHERE (IC130M_3.LLPPN=DE100M.USRLD3) AND (IC130M_3.LLSTLC='MPORTO2')), 
  (SELECT (IC130M_4.LLBLT1+IC130M_4.LLBLT2+IC130M_4.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_4 WHERE (IC130M_4.LLPPN=DE100M.USRLD3) AND (IC130M_4.LLSTLC='MKANB2')), 
  (SELECT (IC130M_5.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_5 WHERE (IC130M_5.LLPPN=DE100M.USRLD3) AND (IC130M_5.LLSTLC='KIPA')) 
  
FROM 
  AUTO.D805DATHPO.DE100M DE100M, 
  AUTO.D805DATHPO.FET001 FET001, 
  AUTO.D805DATHPO.FET001K FET001K, 
  AUTO.D805DATHPO.TSA015 TSA015, 
  AUTO.D805DATHPO.IC130M IC130M, 
  AUTO.D805DATHPO.IC140M IC140M 
  
WHERE 
  FET001K.ETK101=FET001.ET0101 
  AND FET001.ET0102=DE100M.IZPN 
  AND DE100M.USRLD3=TSA015.SA1501 
  AND DE100M.USRLD3=IC130M.LLPPN 
  AND DE100M.USRLD3=IC140M.WBPPN 
  AND ((FET001K.ETK103='R') AND (IC130M.LLSTLC='REC')) 
  
GROUP BY 
  DE100M.USRLD3, 
  DE100M.USRSD3, 
  TSA015.SA1506, 
  TSA015.SA1511, 
  TSA015.SA1512, 
  (IC130M.LLBLT1+IC130M.LLBLT2+IC130M.LLBLT4), 
  (IC140M.WBBLT4-(IC130M.LLBLT1+IC130M.LLBLT2+IC130M.LLBLT4)),
  (SELECT (IC130M_1.LLBLT1+IC130M_1.LLBLT2+IC130M_1.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_1 WHERE (IC130M_1.LLPPN=DE100M.USRLD3) AND (IC130M_1.LLSTLC='MPPORTO')), 
  (SELECT (IC130M_2.LLBLT1+IC130M_2.LLBLT2 + IC130M_2.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_2 WHERE (IC130M_2.LLPPN = DE100M.USRLD3) AND (IC130M_2.LLSTLC='MP-KANB')),
  (SELECT (IC130M_3.LLBLT1+IC130M_3.LLBLT2+IC130M_3.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_3 WHERE (IC130M_3.LLPPN=DE100M.USRLD3) AND (IC130M_3.LLSTLC='MPORTO2')), 
  (SELECT (IC130M_4.LLBLT1+IC130M_4.LLBLT2 + IC130M_4.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_4 WHERE (IC130M_4.LLPPN=DE100M.USRLD3) AND (IC130M_4.LLSTLC='MKANB2')), 
  (SELECT (IC130M_5.LLBLT4) FROM AUTO.D805DATHPO.IC130M IC130M_5 WHERE (IC130M_5.LLPPN=DE100M.USRLD3) AND (IC130M_5.LLSTLC='KIPA')) 
  
ORDER BY TSA015.SA1512
